@Command(
    'bbs.print_situations',
    command_type=CommandType.Live
)
def _bbs_command_print_situations(_connection: int = None):
    from sims4.commands import CheatOutput
    output = CheatOutput(_connection)
    try:
        output(f'Printing Situations.')
        instances: List[Tuple[int, str]] = list()
        for (resource_key, instance) in BBInstanceUtils.get_all_instances(Types.SITUATION, return_type=Situation):
            instance: Situation = instance
            instance_tuning_name = instance.__name__.lower()
            guid64 = getattr(instance, 'guid64', None)
            instances.append((guid64, instance_tuning_name))

        sorted_instances = sorted(instances, key=lambda x: x[1])
        for (_guid64, _instance_tuning_name) in sorted_instances:
            log.debug(f'{_guid64},  # {_instance_tuning_name}')
        output('Done')
    except Exception as ex:
        output('Error occurred.')
        log.error('Problem occurred', exception=ex)


